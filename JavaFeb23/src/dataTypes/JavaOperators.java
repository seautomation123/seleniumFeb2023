package dataTypes;

public class JavaOperators {
	
	/*5 type of operator in java
	 * 1. Arithmetic operator
	 * 2. Assignment operator
	 * 3. comparison/Relational operator
	 * 4. logical operator
	 * 5. Bitwise operator 
	 */

	public static void main(String[] args) {
		
		//Arithmetic operator
		//they are +, -, %, *, ++, --
		
		int a = 10;
		int b = 5;
		int result;
		
		result = a+b;
		System.out.println("Addition result is  " + result);
		result = a-b;
		System.out.println("Subtraction result is  " + result);
		result = a*b;
		System.out.println("Multiplication result is  " + result);
		result = a/b;
		System.out.println("Division result is  " + result);
		result = a%b;
		System.out.println("Modulus result is  " + result);
		//System.out.println("Incremental result is  " + a++);
		//System.out.println("Decremental result is  " + a--);
		
		
		
		
		
		
		
		
		
	}

}
